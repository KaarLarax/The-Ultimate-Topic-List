void solve() {
    string str, a, b, line;
    map<string, string> dic;
    while (getline(cin, line) && !line.empty()) {
        stringstream ss(line);
        ss >> a >> b;
        dic[b] = a;
    }
    while(cin >> str) {
        if (dic.find(str) != dic.end()) {
            cout << dic[str] << edl;
        } else {
            cout << "eh" << edl;
        }
    }
}

